window.getLang = () => localStorage.getItem('lang') || 'bn';
window.setLang = (l) => { localStorage.setItem('lang', l); applyI18n(); document.dispatchEvent(new Event('langchange')); };
window.t = (key) => (I18N[getLang()] && I18N[getLang()][key]) || (I18N.en[key]) || key;

function applyI18n() {
  const lang = getLang();
  document.documentElement.lang = lang;
  document.documentElement.setAttribute('data-lang', lang);
  document.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.getAttribute('data-i18n')); });
  document.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.placeholder = t(el.getAttribute('data-i18n-ph')); });
  const tg = document.getElementById('langToggle');
  if (tg) tg.textContent = lang === 'bn' ? 'EN' : 'বাং';
}
window.applyI18n = applyI18n;
// pick the right field by language: bn falls back to en field
window.L = (obj, base) => {
  if (!obj) return '';
  const bn = obj[base + 'Bn'];
  return getLang() === 'bn' && bn ? bn : (obj[base] || bn || '');
};
